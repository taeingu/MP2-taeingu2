    @Test(timeOut = SIMPLE_TEST_TIMEOUT)
    public void testStringSplitterWithGoodInputs() {
        Assert.assertEquals(StringSplitter.stringSplitter(""), new String[] {},
                "Incorrect result for empty string");
        Assert.assertEquals(StringSplitter.stringSplitter("AA"), new String[] { "AA" },
                "Incorrect result for simple string");
        Assert.assertEquals(StringSplitter.stringSplitter("AB"), new String[] { "A", "B" },
                "Incorrect result for simple string");
        Assert.assertEquals(StringSplitter.stringSplitter("A A"), new String[] { "A", " ", "A" },
                "Incorrect result for string with spaces");
    }

    @Test(timeOut = SIMPLE_TEST_TIMEOUT)
    public void testStringSplitterWithBadInputs() {
        Assert.assertNull(StringSplitter.stringSplitter(null), "Incorrect result for null String");
    }

